#!/usr/bin/env python3

if __name__ == '__main__':
    print(11 / 2)  # Division
    # return: 5.5

    print(11 // 2)  # Integer division. Rounds down to the nearest integer.
    # return: 5

    print(-11 // 2)
    # return: -6

    print(11.0 // 2)  # Returns a float (rounded down to the nearest integer) because the first argument is a float.
    # return: 5.0

    print(11**2)  # Raise to the power of
    # return: 121

    print(11 % 2)  # Returns the remainder
    # return: 1
